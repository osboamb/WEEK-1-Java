public class FloatingPointTest {
    public static void main (String[] args) {

        float myfloat = 1.36f;
        double mydouble = 1.36d;

        System.out.println(myfloat == mydouble);

        float myf = 0.1f;

        System.out.println(myf + 0.6f);

        float f = 1f / 49f;

        System.out.println(f * 49f);

    }
}